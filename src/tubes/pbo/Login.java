private void userLogin(String username,String password){
        Connection connect = data.connectDB();
        if(connect != null){
        try {
            PreparedStatement st = (PreparedStatement)
            connect.prepareStatement("Select * from user WHERE username = ? AND password = ? ");
            st.setString(1,username);
            st.setString(2,password);
            
            ResultSet rs = st.executeQuery();
            if(rs.next()){               
                // MENAMPILKAN store SETELAH LOGIN
                dispose();
                Page1 d = new Page1();
                d.setTitle("Store menu");
                d.setVisible(true);
//                welcomeName.setText(rs.getString((2)));
//                new dashboard_users().setVisible(true);
            }else {
                JOptionPane.showMessageDialog(this,"Make sure to type the correct Username and Password", "Error",JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        }else{
            System.out.println("Databse tidak tersedia");
        }
        
    }
